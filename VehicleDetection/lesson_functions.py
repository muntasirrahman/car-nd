def show_image(image_list, label_list=None, cols=2, fig_w=18, fig_h=12, show_axis='on', debug=False):
    if label_list is None:
        label_list = []
        label_found = False
    else:
        label_found = True

    n_img = len(image_list)
    if n_img < 1 and debug:
        print('No image to be shown'.format(n_img))
        pass

    rows = n_img // cols
    if rows == 0:
        rows = 1
    if rows * cols < n_img:
        rows += 1

    gs = gridspec.GridSpec(rows, cols)
    plt.close('all')
    plt.figure(figsize=(fig_w, fig_h))

    matplotlib.rcParams.update({'font.size': 12})

    for i, img in enumerate(image_list):
        # fig = plt.subplot(gs[i])
        plt.subplot(gs[i])
        if label_found:
            # fig.set_title(label_list[i], fontsize=10)
            plt.title(label_list[i])

        # fig.imshow(img, cmap='gray')
        # fig.axis(show_axis)
        plt.imshow(img, cmap="gray")
        plt.axis(show_axis)

    plt.tight_layout(pad=0.4, w_pad=0.5, h_pad=0.5)
    plt.show()


def get_hog_features(img, orient, pix_per_cell, cell_per_block, vis=False, feature_vec=True):
    # Call with two outputs if vis==True
    if vis:
        features, hog_image = hog(img, orientations=orient,
                                  pixels_per_cell=(pix_per_cell, pix_per_cell),
                                  cells_per_block=(cell_per_block, cell_per_block),
                                  transform_sqrt=True,
                                  block_norm='L2-Hys',
                                  visualise=vis, feature_vector=feature_vec)
        return features, hog_image
    # Otherwise call with one output
    else:
        features = hog(img, orientations=orient,
                       pixels_per_cell=(pix_per_cell, pix_per_cell),
                       cells_per_block=(cell_per_block, cell_per_block),
                       transform_sqrt=True,
                       block_norm='L2-Hys',
                       visualise=vis, feature_vector=feature_vec)
        return features


